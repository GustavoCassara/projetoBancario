public class Cliente {
    private String nome;
    private String tipoConta;

    public Cliente(){
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getTipoConta() {
        return tipoConta;
    }

    public void setConta(String conta) {
        this.tipoConta = conta;
    }
}
